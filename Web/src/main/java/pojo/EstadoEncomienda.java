package pojo;

public class EstadoEncomienda {
	private int idEstadoEncomienda;
	private String descripcionEstadoEncomienda;
	
	public int getIdEstadoEncomienda() {
		return idEstadoEncomienda;
	}
	public void setIdEstadoEncomienda(int idEstadoEncomienda) {
		this.idEstadoEncomienda = idEstadoEncomienda;
	}
	public String getDescripcionEstadoEncomienda() {
		return descripcionEstadoEncomienda;
	}
	public void setDescripcionEstadoEncomienda(String descripcionEstadoEncomienda) {
		this.descripcionEstadoEncomienda = descripcionEstadoEncomienda;
	}
}
